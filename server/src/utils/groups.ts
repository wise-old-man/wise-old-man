function findGroupRole(roleName: string): GroupRole | null {
  for (const [key, value] of Object.entries(GroupRoleProps)) {
    if (value.name.toUpperCase() === roleName.toUpperCase()) {
      return key as GroupRole;
    }
  }

  return null;
}

function isGroupRole(roleString: string): roleString is GroupRole {
  return roleString in GroupRoleProps;
}

